#2. Crie um programa que continue pedindo, repetidas vezes, para que o usuário insira um número qualquer. O programa deve se encerrar 
# somente quando o usuário inserir o valor 0 (zero).

num = int(input("Digite o valor de um número :"))

while num!=0:
    num = int(input("Digite o valor de um número: "))
print("Programa encerrado")
    

